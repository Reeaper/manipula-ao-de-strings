l = [5,7,2,9,4,1,3]

print('Lista: ',l)

# len
print('Quantos itens tem na lista? ',len(l))
# min
print('Valor minimo da lista: ',min(l))
# max
print('Valor maximo da lista: ',max(l))
# sum
print('Soma de todos os valores da lista: ',sum(l))
# in
print('Valor 3 consta na lista? ',3 in l)

# sort
l.sort()
print('Lista em ordem crescente: ',l)

# reverse
l.reverse()
print('Lista em ordem decrescente(invertida): ',l)